/**
 * Represents different types of power-ups in the game.
 */
public enum PowerUp {
    /**
     * Reveals a safe tile automatically.
     */
    REVEAL_SAFE, //these are enums - this is something new!
    
    /**
     * Grants an extra life.
     */
    EXTRA_LIFE,
    
    /**
     * Reveals all adjacent tiles.
     */
    REVEAL_ADJACENT
}
